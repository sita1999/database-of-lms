    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
    model.getDataVector().removeAllElements();
       table.revalidate();
        ArrayList<Student> list= studentDb.searchStudent(txt_name.getText(), txt_roll.getText());
       Object rows[]=new Object[11];
       for(Student s1:list){
           rows[0]=s1.getStudentId();
           rows[1]=s1.getStudentname();
           rows[2]=s1.getRollno();
           rows[3]=s1.getFathername();
           rows[4]=s1.getBranchname();
           rows[5]=s1.getYear();
           rows[6]=s1.getSection();
           rows[7]=s1.getEmail();
           rows[8]=s1.getContactno();
           rows[9]=s1.getAddressname();
           rows[10]=s1.getDateofbirth();
           model.addRow(rows);
       }
    }//GEN-LAST:event_btn_SearchActionPerformed

