    private void loadData() {
       ArrayList<Student> list= studentDb.allstudent();
       model.getDataVector().removeAllElements();
       table.revalidate();
       Object rows[]=new Object[11];
       for(Student s1:list){
           rows[0]=s1.getStudentId();
           rows[1]=s1.getStudentname();
           rows[2]=s1.getRollno();
           rows[3]=s1.getFathername();
           rows[4]=s1.getBranchname();
           rows[5]=s1.getYear();
           rows[6]=s1.getSection();
           rows[7]=s1.getEmail();
           rows[8]=s1.getContactno();
           rows[9]=s1.getAddressname();
           rows[10]=s1.getDateofbirth();
           model.addRow(rows);
       }
    }
