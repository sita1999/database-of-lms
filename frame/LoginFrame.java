    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       
        
        if (txtUsername.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Must be enter username.");
            return;
        }
        if (String.valueOf(txtPassword.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "Must be enter password.");
            return;
        }
        UserDB userDB = new UserDB();
        if (userDB.isUserExist(new User(txtUsername.getText(), String.valueOf(txtPassword.getPassword())))) {
            new Dashboard().setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Username/Password invalid.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

